const int MAX_TESTPED_LENGTH = 6;
struct SimpleTestPed {
    uint64_t ped[MAX_TESTPED_LENGTH];
    int length;

    static inline uint64_t compute_max_ped(int NUM_PED_TERMS,
                                           int MAX_RANK) {
        uint64_t MAX_PED = 1;
        for (int i = 0; i < NUM_PED_TERMS; ++i) {
            MAX_PED *= (MAX_RANK+1);
        }
        return MAX_PED;
    }


    // Fills in a reverse pedigree array from an integer encoding.
    static inline
    void fill_rev_ped_from_int_helper(int64_t ped_val,
                                      int MAX_RANK,
                                      int NUM_PED_TERMS,
                                      uint64_t* rev_pedigree,
                                      int* ped_length,
                                      uint64_t* rev_stem,
                                      int stem_length,
                                      int strip_zeros)
    {
        int64_t tmp = ped_val;
        *ped_length = 0;

        uint64_t* tmp_ped = (uint64_t*) alloca(sizeof(uint64_t) * NUM_PED_TERMS);

        for (int i = NUM_PED_TERMS-1; i >= 0; --i) {
            tmp_ped[i] = tmp % (MAX_RANK+1);
            tmp = tmp / (MAX_RANK+1);
        }

        *ped_length = NUM_PED_TERMS;

        if (strip_zeros) {
            // Strip off zero terms at the end of the pedigree.
            while ((*ped_length > 0) &&
                   (tmp_ped[*ped_length-1] == 0)) {
                *ped_length = *ped_length - 1;
            }
        }

        // Store the reverse pedigree back into the array.
        for (int q = *ped_length -1; q >= 0; q--) {
            rev_pedigree[(*ped_length - 1) - q] = tmp_ped[q];
        }

        if (0) {
            fprintf(stderr, "Ped term %lld: ", ped_val);
//        print_rev_pedigree(rev_pedigree, *ped_length);
            fprintf(stderr, "\n");
        }

        // Deal with the first term of the common stem as a special case.
        if (*ped_length == 0) {
            // We strippped off all the terms (because they were 0).
            *ped_length = 1;
            rev_pedigree[0] = rev_stem[0];
        }
        else {
            rev_pedigree[(*ped_length)-1] += rev_stem[0];
        }
    
        // Append the rest of the common stem on the end.
        for (int q = 1; q < stem_length; ++q) {
            rev_pedigree[(*ped_length) -1 + q] = rev_stem[q];
        }
        *ped_length = *ped_length + stem_length-1;

        if (0) {
            fprintf(stderr, "Ped term %lld: ", ped_val);
//        print_rev_pedigree(rev_pedigree, *ped_length);
            fprintf(stderr, "\n");
        }
    }

    static inline
    void fill_rev_ped_from_int(int64_t ped_val,
                               int MAX_RANK,
                               int NUM_PED_TERMS,
                               uint64_t* rev_pedigree,
                               int* ped_length,
                               uint64_t* rev_stem,
                               int stem_length)
    {
        fill_rev_ped_from_int_helper(ped_val, MAX_RANK, NUM_PED_TERMS,
                                     rev_pedigree, ped_length,
                                     rev_stem, stem_length,
                                     1);
    }

    static inline 
    void generate_test_ped_list(int max_num_terms, int max_rank_val)
    {
        assert(max_num_terms < MAX_TESTPED_LENGTH);
        int max_rank = max_rank_val + 1;
        std::vector<pedigree> ped_list;
        uint64_t common_stem[1] = {0};
        uint64_t* buffer = new uint64_t[max_num_terms + 1];

        // Generate the list of pedigrees.
        for (int nt = 1;  nt < max_num_terms; ++nt) {
            int max_ped = compute_max_ped(nt, max_rank);
            for (int k = 0; k < max_ped; ++k) {
                int L;
                fill_rev_ped_from_int_helper(k, max_rank, nt,
                                             buffer, &L,
                                             common_stem, 1, 0);

                pedigree test_ped = pedigree(buffer, L, true);
                ped_list.push_back(test_ped);
            }
        }

        int num_terms = 0;
        // Sort the terms.
	std::sort(ped_list.begin(), ped_list.end());
        fprintf(stderr, "const int TESTPED_LENGTH = %zu; \n",
                ped_list.size());
        fprintf(stderr, "SimpleTestPed PED_LIST[TESTPED_LENGTH] = { \n");
        for (size_t i = 0; i < ped_list.size(); ++i) {
            pedigree test_ped = ped_list[i];
            fprintf(stderr, "    { { ");
            for (int j = 0; j < test_ped.length()-1; ++j) {
                fprintf(stderr, "%lld, ",
                        (long long)test_ped[j]);
            }
            fprintf(stderr, "%lld }, %d}, \n",
                    (long long)test_ped[test_ped.length()-1],
                    test_ped.length());
        }
        fprintf(stderr, "}; \n");
        delete[] buffer;
    }
};

